#define MMP2_ICU_INT9_STATUS		ICU_REG(0x180)
#define MMP2_ICU_INT55_STATUS		ICU_REG(0x188)

#define MMP2_ICU_INT4_MASK		ICU_REG(0x168)
#define MMP2_ICU_INT5_MASK		ICU_REG(0x16C)
